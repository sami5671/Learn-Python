'''
Strings are immutable that means, once it is created it can not be changed
'''

# single quotes 
string1 = 'Hello, world!'

# double quotes
string2 = "Hello, world!"

# triple quotes 
string3 = '''Hello,
world!'''

# Triple quotes can span multiple lines
string4 = """Hello,
World!"""


print(string1)
print(string2)
print(string3)
print(string4)
